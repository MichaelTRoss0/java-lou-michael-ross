public class BestAdderEver {
    public static void main(String[] args) {
        
        // Store three different numbers in separate variables,
        // display their values on the screen,
        // add their values together in and store the result in a new variable.
        
        int a = 3;
        int b = 5;
        int c = 7;
        
        System.out.println("a = " + a);
        System.out.println("b = " + b);
        System.out.println("c = " + c);
        
        int d = a + b + c;
        
        System.out.println("d = a + b + c = " + d);
    }
}
